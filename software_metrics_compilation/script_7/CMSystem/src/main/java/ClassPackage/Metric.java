package ClassPackage;

public abstract class Metric
{
	protected String name;

	public Metric() {
		super();

	}
	public Metric(String name) {
		super();
		this.name = name;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	
	
	
	

}